    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (txtBoxFName.getText().equals("") || txtBoxPassword.getPassword().equals("") || txtBoxUsername.getText().equals("")) {
            jLabel6.setText("Fill all data");
        } else {
            MongoClient mongoClient = new MongoClient();
            MongoDatabase db = mongoClient.getDatabase("PhoneBook");
            System.out.print(db.getName());

            db.getCollection("User").insertOne(
                    new Document("Name", new Document()
                            .append("first", txtBoxFName.getText())
                            .append("last", txtBoxLName.getText()))
                    .append("Password", String.valueOf(txtBoxPassword.getPassword()))
                    .append("username", txtBoxUsername.getText())
                    .append("Contact", asList(
                            new Document()
                            .append("name", txtBoxFName.getText())
                            .append("phone", "xxxxxxxxxxxx")
                            .append("email", "mail@gmail.com"))
                    )
            );
            this.setVisible(false);
            Login log = new Login();
            log.setVisible(true);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

